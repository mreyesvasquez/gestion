                    },
                    beforeSend: function(){
                        $("#waiting1").html('<img src="http://webdeveloper.decontsoft.com/sys_sciudadana/assets/images/loaders/loader.gif" alt="" style="margin: 5px;" />');
                    },
                    error:function(){
                        $("#waiting1").html('Hubo un error mientras se cargaba los datos.');
                    }
                });
            });
        });
    });
