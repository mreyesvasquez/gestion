    $(window).load(function() {
        $('.redB').click(function(){
             $("#validate").validate({
                rules: {
                    zona: { 
                        required: true,    
                    },
                    region:{
                        required: true,
                    },
                    provincia:{
                        required: true,
                    },
                    distrito:{
                        required: true,
                    }
                },
                messages: { 
                    zona: {
                        required:"Por favor ingrese una descripción para la zona."
                     },
                     region:{
                        required:"Seleccione una región para continuar."
                     },
                     provincia:{
                         required:"Seleccione una provincia para continuar."
                     },
                     distrito:{
                         required:"Seleccione un distrito para continuar."
                     }
                },
                submitHandler: function(form) {
                    $(form).ajaxSubmit({
                        url:"http://webdeveloper.decontsoft.com/sys_sciudadana/admin/newZona",
                        type:"post",
                        success: function(data){
                            document.getElementById("submit").disabled = false;
                            $("#resultados").html("");
                            $.jGrowl(data, { header: 'Mensaje del Sistema' });
                        },
                        beforeSend: function(){
                            $("#resultados").html('<img src="http://webdeveloper.decontsoft.com/sys_sciudadana/assets/images/loaders/loader.gif" alt="" style="margin: 5px;" />');
                            document.getElementById("submit").disabled = true;
                        },
                        error:function(){
                            $("#resultados").html('Hubo un error mientras se insertaba los datos.');
                        }
                    });
              }
            }); 
        });
        $("#region").change(function() {
            $("#region option:selected").each(function() {
                var departamento = $('#region').val();
                $.ajax({
                    url: "http://webdeveloper.decontsoft.com/sys_sciudadana/admin/cargarProvincias",
                    type: "post",
                    data: "departamento="+departamento,
                    success: function(data){
                        $("#provincia").html(data);
                        $("#waiting").html('');
                    },
                    beforeSend: function(){
                        $("#waiting").html('<img src="http://webdeveloper.decontsoft.com/sys_sciudadana/assets/images/loaders/loader.gif" alt="" style="margin: 5px;" />');
                    },
                    error:function(){
                        $("#waiting").html('Hubo un error mientras se cargaba los datos.');
                    }
                });
            });
        });
        $("#provincia").change(function() {
            $("#provincia option:selected").each(function() {
                var provincia = $('#provincia').val();
                $.ajax({
                    url: "http://webdeveloper.decontsoft.com/sys_sciudadana/admin/cargarDistritos",
                    type: "post",
                    data: "provincia="+provincia,
                    success: function(data){
                        $("#distrito").html(data);
